package com.xworkz.inheritance.things;

public class Dowry {

	public boolean cash;
	public long amount;
	public boolean crime;

	public Dowry() {
		super();
	}

	public Dowry(boolean cash, long amount, boolean crime) {
		super();
		this.cash = cash;
		this.amount = amount;
		this.crime = crime;
	}

}
