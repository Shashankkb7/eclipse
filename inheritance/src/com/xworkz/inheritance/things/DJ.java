package com.xworkz.inheritance.things;

public class DJ {

	public String name;
	public int noOfSingers;
	public String type;

	public DJ() {
		super();
	}

	public DJ(String name, int noOfSingers, String type) {
		super();
		this.name = name;
		this.noOfSingers = noOfSingers;
		this.type = type;
	}

}
