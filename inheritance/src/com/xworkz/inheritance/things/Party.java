package com.xworkz.inheritance.things;

public class Party {

	public String type;
	public String location;
	public int noOfMembers;

	public Party() {
		super();
	}

	public Party(String type, String location, int noOfMembers) {
		super();
		this.type = type;
		this.location = location;
		this.noOfMembers = noOfMembers;
	}

}
